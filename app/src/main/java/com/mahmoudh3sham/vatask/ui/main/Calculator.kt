package com.mahmoudh3sham.vatask.ui.main

class Calculator {
    companion object {
        fun add(firstNum: Double, secondNum: Double): Double {
            return firstNum + secondNum
        }

        fun sub(firstNum: Double, secondNum: Double): Double {
            return firstNum - secondNum
        }

        fun div(firstNum: Double, secondNum: Double): Double {
            return firstNum / secondNum
        }

        fun mul(firstNum: Double, secondNum: Double): Double {
            return firstNum * secondNum
        }
    }
    
}